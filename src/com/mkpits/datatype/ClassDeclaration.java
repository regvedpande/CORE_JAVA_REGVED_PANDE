package com.mkpits.datatype;

//Declaration of  the variables
public class ClassDeclaration {
	int a;
	float b;
	double c;
	String s;

	// By Using Constructor we can initialize data types
	public ClassDeclaration(int a, float b, double c, String s) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
		this.s = s;
	}

}
